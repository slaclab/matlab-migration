function DumpDataFromSCORE(data,comment,ts,title)

n = size(data,2);
for i=1:n
    disp(sprintf('%d %s %s area=%s rb pv=%s rb val=%f sp pv=%s sp val=%f', ...
        i, data{i}.region, ...
        data{i}.aliasName, ...
        data{i}.area, ...
        data{i}.readbackName, ...
        data{i}.readbackVal, ...
        data{i}.setpointName, ...
        data{i}.setpointVal));
end
disp([comment ' ' char(ts) ' ' title]);
